{
    const char *dbName[] = {"dbA", "dbB", "dbC"};
    const int dbTimeStates[] = {10, 20, 30};
    const double dbStartCycle[] = {0., 10., 0.};
    const double dbEndCycle[] = {9., 29., 29.};
    const double dbStartTime[] = {14., 16., 0.};
    const double dbEndTime[] = {18.5, 35., 29.};
    const double dbXMin[] = {0., 0., 5.1};
    const double dbXMax[] = {5., 5., 10.1};
    const double dbYMin[] = {0., 5.1, 5.1};
    const double dbYMax[] = {5., 10.1, 10.1};

    for(int dbIndex = 0; dbIndex < 3; ++dbIndex)
    {
        int timeStates = dbTimeStates[dbIndex];
        int kmax = 61, lmax = 61;

        // Make the program use the right letter depending on the database.
        const unsigned char *data;
        if(dbIndex == 0)
            data = a_alpha;
        else if(dbIndex == 1)
            data = b_alpha;
        else 
            data = c_alpha;

        FieldWithData<double> rt(timeStates, 1, kmax, lmax, 0., true);
        FieldWithData<double> zt(timeStates, 1, kmax, lmax, 0., true);
        FieldWithData<int>    ireg(timeStates, 1, kmax, lmax, 1, false);

        // Mixed material information.
        FieldWithData<int>    nummm(timeStates, 1, kmax, lmax, 0, false);
        FieldWithData<int>    ilamm(timeStates, 1, kmax*2, lmax, 0, false);
        LinearArray<int>      iregmm;
        LinearArray<double>   volfmm;

        //
        // Populate the rt and zt arrays at timestate 0.
        //
        double xmin = dbXMin[dbIndex], xmax = dbXMax[dbIndex];
        double ymin = dbYMin[dbIndex], ymax = dbYMax[dbIndex];
        int i, j;
        for(j = 0; j < lmax; ++j)
        {
            double ty = double(j) / double(lmax-1);
            double Y = ty * ymax + (1. - ty) * ymin;
            for(i = 0; i < kmax; ++i)
            {
                double tx = double(i) / double(kmax-1);
                double X = tx * xmax + (1. - tx) * xmin;

                for(int ts = 0; ts < timeStates; ++ts)
                {
                    rt.SetValue(ts, 0, i, j, Y);
                    zt.SetValue(ts, 0, i, j, X);
                }
            }
        }

        //
        // Figure out the materials.
        //
        const int mixSizePerTimeState = int(float(kmax * lmax) / 3.7f);
        int ic = (kmax-1) / 2;
        int jc = (lmax-1) / 2;
        int ts;
        for(ts = 0; ts < timeStates; ++ts)
        {
            int array_index = 1;
            double tst = double(ts) / double(timeStates - 1);
            double timeAngle = tst * 2. * 3.14159;
            double cX = zt.GetValue(ts, 0, ic, jc);
            double cY = rt.GetValue(ts, 0, ic, jc);

            for(j = 1; j < lmax; ++j)
            {
                const unsigned char *letterRow = data + (lmax - j) * kmax;
                for(i = 1; i < kmax; ++i)
                {
#define MAXMATS 5
                    double matvf[MAXMATS];
                    int matsInZone[MAXMATS];
                    int nmats = 0;
                    int matno = 0;

                    for(int n = 0; n < MAXMATS; ++n)
                    {
                        matsInZone[n] = 0;
                        matvf[n] = 0;
                    }

                    if(letterRow[i] == 255)
                    {
                        // The letter was fully opaque.
                        matno = 4;
                        nmats = 1;
                        ++matsInZone[4];
                        matvf[4] = 1.;
                    }
                    else
                    {
                        double X[4], Y[4];

#define GetValues(DEST, FIELD) \
                DEST[0] = FIELD.GetValue(ts, 0, i, j);\
                DEST[1] = FIELD.GetValue(ts, 0, i-1, j);\
                DEST[2] = FIELD.GetValue(ts, 0, i-1, j-1);\
                DEST[3] = FIELD.GetValue(ts, 0, i, j-1);

                        GetValues(X, zt);
                        GetValues(Y, rt);

                        const int xSamples = 10;
                        const int ySamples = 10;

                        // The letter was not fully opaque.
                        for(int ys = 0; ys < ySamples; ++ys)
                        {
                            double yt = double(ys) / double(ySamples - 1);
                            double one_minus_yt = 1. - yt;
                            for(int xs = 0; xs < xSamples; ++xs)
                            {
                                double xt = double(xs) / double(xSamples - 1);
                                double one_minus_xt = 1. - xt;

                                double TempLeft, TempRight;
                                double pX, pY;

#define BiLinearInterpolate(DEST, VALS) \
                                TempLeft  = one_minus_yt * VALS[2] + yt * VALS[1]; \
                                TempRight = one_minus_yt * VALS[3] + yt * VALS[0]; \
                                DEST = one_minus_xt * TempLeft + xt * TempRight;
    
                                BiLinearInterpolate(pX, X);
                                BiLinearInterpolate(pY, Y);
                                double dX = pX - cX;
                                double dY = pY - cY;
                                double R = sqrt(dX*dX + dY*dY);
                                double angle = ComputeAngle(dX, dY);

                                // Try to create something like a backwards
                                // spinning clock.
                                if(R > 2.)
                                    ++matsInZone[2];
                                else if(angle >= 0 && angle <= timeAngle)
                                    ++matsInZone[1];
                                else
                                    ++matsInZone[3];
                            }
                        }

                        // Figure out the material volume fractions for non-letter mats.
                        const double nTotalSamples = double(ySamples * xSamples);
                        int n;
                        for(n = 0; n < MAXMATS-1; ++n)
                        {
                            if(matsInZone[n] > 0)
                               ++nmats;
                            matvf[n] = double(matsInZone[n]) / nTotalSamples;
                        }

                        // If the lette was present in any way, adjust the volume fractions.
                        if(letterRow[i] > 0)
                        {
                            ++matsInZone[4];

                            ++nmats;
                            double letterVF = double(letterRow[i]) / 255.;
                            // Set the volume fraction for the letter.
                            matvf[4] = letterVF;

                            // Reduce the other volume fractions so it all still sums to 1.
                            double rest = 1. - letterVF;
                            for(n = 0; n < MAXMATS-1; ++n)
                                matvf[n] *= rest;
                        }

                        // Look for the material that occupies most of the cell.
                        matno = 0;
                        for(n = 1; n < MAXMATS; ++n)
                        {
                            if(matvf[n] > matvf[matno])
                                matno = n;
                        }
                    }

                    if(nmats > 1)
                    {
                        // Save the indices into the iregmm and volfmm arrays.
                        ilamm.SetValue(ts, 0, 2*i, j, array_index);
                        ilamm.SetValue(ts, 0, 2*i+1, j, 0);
                        nummm.SetValue(ts, 0, i, j, nmats);
            
                        for(int n = 0; n < MAXMATS; ++n)
                        {
                            if(matsInZone[n] > 0)
                            {
                                ++array_index;
                                volfmm.AddValue(matvf[n]);
                                iregmm.AddValue(n);
                            }
                        }
                    }
                    else
                    {
                        // Save the indices into the iregmm and volfmm arrays.
                        ilamm.SetValue(ts, 0, 2*i, j, 0);
                        ilamm.SetValue(ts, 0, 2*i+1, j, 0);
                        nummm.SetValue(ts, 0, i, j, 0);
                    }
    
                    ireg.SetValue(ts, 0, i, j, matno);
                }
            }

            // Pad the iregmm and volfmm arrays so this time state contains
            // mixSizePerTimeState values.
            int delta = mixSizePerTimeState - array_index;
            for(int idx = 0; idx < delta; ++idx)
            {
                volfmm.AddValue(0.);
                iregmm.AddValue(0);
            }
        }

        //
        // Create the PDB file and set up the fields that we want to write to it.
        //
        FieldWriter pdb;

        // Set the database's creation comment.
        pdb.SetDatabaseComment(string("Flash database: ") +
            string(dbName[dbIndex]) +
            string(" for testing VisIt's database correlations."));

        // Set up the times and cycles.
        for(ts = 0; ts < timeStates; ++ts)
        {
             double t = double(ts) / double(timeStates - 1);
             double omt = 1. - t;

             // Figure out the cycle and the time.
             int   cycle = int(rint(omt * dbStartCycle[dbIndex] + t * dbEndCycle[dbIndex]));
             double time = omt * dbStartTime[dbIndex] + t * dbEndTime[dbIndex];

             // Add a new time state.
             pdb.AddTimeStep(cycle, time);
        }

        //
        // Add fields to the pdb file.
        //
        pdb.AddField("rt", "rt@value", &rt);
        pdb.AddField("zt", "zt@value", &zt);
        pdb.AddField("ireg", "ireg@value", &ireg);

        pdb.AddField("nummm", "nummm@las", &nummm);
        pdb.AddField("ilamm", "ilamm@las", &ilamm);
        pdb.AddField("iregmm", "iregmm@las", &iregmm);
        pdb.AddField("volfmm", "volfmm@las", &volfmm);

        //
        // Write the data to a single PDB file.
        //
        pdb.WriteData(dbName[dbIndex], kmax, lmax, timeStates, true);
    }

    return 0;
}
