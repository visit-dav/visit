double
ComputeAngle(double dX, double dY)
{
    double angle = 0.;
    if(dX >= 0.)
    {
        if(dY >= 0.)
        {
            // Q 1
            if(dX == 0)
                angle = M_PI;
            else
                angle = atan(dY / dX);
        }
        else
        {   // Q 4
            angle = 1.5 * M_PI + atan(dX / -dY);
        }
    }
    else
    {
        if(dY >= 0.)
        {
            // Q 2
            if(dY == 0)
                angle = M_PI;
            else
                angle = M_PI * 0.5 +  atan(-dX / dY);  
        }
        else
        {
            // Q 3
            angle = M_PI + atan(dY / dX);
        }
    }

    return angle;
}

