#ifndef EXTRUDESTACKED_PLUGIN_INFO_H
#define EXTRUDESTACKED_PLUGIN_INFO_H
#include <OperatorPluginInfo.h>
#include <operator_plugin_exports.h>

class ExtrudeStackedAttributes;

// ****************************************************************************
//  Class: ExtrudeStackedPluginInfo
//
//  Purpose:
//    Five classes that provide all the information about an ExtrudeStacked operator
//
//  Programmer: generated by xml2info
//  Creation:   omitted
//
//  Modifications:
//
// ****************************************************************************

class ExtrudeStackedGeneralPluginInfo : public virtual GeneralOperatorPluginInfo
{
  public:
    virtual const char *GetName() const;
    virtual const char *GetVersion() const;
    virtual const char *GetID() const;
    virtual bool  EnabledByDefault() const;
    virtual const char *GetCategoryName() const;
};

class ExtrudeStackedCommonPluginInfo : public virtual CommonOperatorPluginInfo, public virtual ExtrudeStackedGeneralPluginInfo
{
  public:
    virtual AttributeSubject *AllocAttributes();
    virtual void CopyAttributes(AttributeSubject *to, AttributeSubject *from);
};

class ExtrudeStackedGUIPluginInfo : public virtual GUIOperatorPluginInfo, public virtual ExtrudeStackedCommonPluginInfo
{
  public:
    virtual QString *GetMenuName() const;
    virtual QvisPostableWindowObserver *CreatePluginWindow(int type,
        AttributeSubject *attr, const QString &caption, const QString &shortName,
        QvisNotepadArea *notepad);
    virtual const char **XPMIconData() const;
};

class ExtrudeStackedViewerEnginePluginInfo : public virtual ViewerEngineOperatorPluginInfo, public virtual ExtrudeStackedCommonPluginInfo
{
  public:
    virtual AttributeSubject *GetClientAtts();
    virtual AttributeSubject *GetDefaultAtts();
    virtual void SetClientAtts(AttributeSubject *atts);
    virtual void GetClientAtts(AttributeSubject *atts);

    virtual void InitializeOperatorAtts(AttributeSubject *atts,
                                        const avtPlotMetaData &plot,
                                        const bool fromDefault);
    virtual void UpdateOperatorAtts(AttributeSubject *atts,
                                    const avtPlotMetaData &plot);
    virtual const char *GetMenuName() const;

    static void InitializeGlobalObjects();
  private:
    static ExtrudeStackedAttributes *defaultAtts;
    static ExtrudeStackedAttributes *clientAtts;
};

class ExtrudeStackedViewerPluginInfo : public virtual ViewerOperatorPluginInfo, public virtual ExtrudeStackedViewerEnginePluginInfo
{
  public:
    virtual const char **XPMIconData() const;
};

class ExtrudeStackedEnginePluginInfo : public virtual EngineOperatorPluginInfo, public virtual ExtrudeStackedViewerEnginePluginInfo
{
  public:
    virtual avtPluginFilter *AllocAvtPluginFilter();
};

class ExtrudeStackedScriptingPluginInfo : public virtual ScriptingOperatorPluginInfo, public virtual ExtrudeStackedCommonPluginInfo
{
  public:
    virtual void InitializePlugin(AttributeSubject *subj, void *data);
    virtual void *GetMethodTable(int *nMethods);
    virtual bool TypesMatch(void *pyobject);
    virtual char *GetLogString();
    virtual void SetDefaults(const AttributeSubject *atts);
};

#endif
