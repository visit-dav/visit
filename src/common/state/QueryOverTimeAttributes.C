// ****************************************************************************
// Method: QueryOverTimeAttributes::CreateNode
//
// Purpose: 
//   This method creates a DataNode representation of the object so it can be 
//   saved to a config file.
//
// Programmer: Kathleen Bonnell 
// Creation:   May 26, 2004
