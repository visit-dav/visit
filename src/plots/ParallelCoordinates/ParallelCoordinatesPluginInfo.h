#ifndef PARALLELCOORDINATES_PLUGIN_INFO_H
#define PARALLELCOORDINATES_PLUGIN_INFO_H
#include <PlotPluginInfo.h>
#include <plot_plugin_exports.h>

class ParallelCoordinatesAttributes;

// ****************************************************************************
//  Class: ParallelCoordinatesPluginInfo
//
//  Purpose:
//    Five classes that provide all the information about a ParallelCoordinates
//    plot plugin.  The information is broken up into five classes since
//    portions of it are only relevant to particular components within
//    visit.  There is the general information which all the components
//    are interested in, the gui information which the gui is interested in,
//    the viewer information which the viewer is interested in, the
//    engine information which the engine is interested in, and finally a.
//    scripting portion that enables the Python VisIt extension to use the
//    plugin.
//
//  Programmer: generated by xml2info
//  Creation:   omitted
//
// ****************************************************************************

class ParallelCoordinatesGeneralPluginInfo: public virtual GeneralPlotPluginInfo
{
  public:
    virtual const char *GetName() const;
    virtual const char *GetVersion() const;
    virtual const char *GetID() const;
    virtual bool  EnabledByDefault() const;
};

class ParallelCoordinatesCommonPluginInfo : public virtual CommonPlotPluginInfo, public virtual ParallelCoordinatesGeneralPluginInfo
{
  public:
    virtual AttributeSubject *AllocAttributes();
    virtual void CopyAttributes(AttributeSubject *to, AttributeSubject *from);
    virtual int GetVariableTypes() const;
};

class ParallelCoordinatesGUIPluginInfo : public virtual GUIPlotPluginInfo, public virtual ParallelCoordinatesCommonPluginInfo
{
  public:
    virtual QString *GetMenuName() const;
    virtual QvisPostableWindowObserver *CreatePluginWindow(int type,
        AttributeSubject *attr, const QString &caption, const QString &shortName,
        QvisNotepadArea *notepad);
    virtual QvisWizard *CreatePluginWizard(AttributeSubject *attr, QWidget *parent,
        const std::string &varName, const avtDatabaseMetaData *md,
        const ExpressionList *expList);
    virtual const char **XPMIconData() const;
};

class ParallelCoordinatesViewerEnginePluginInfo : public virtual ViewerEnginePlotPluginInfo, public virtual ParallelCoordinatesCommonPluginInfo
{
  public:
    virtual AttributeSubject *GetClientAtts();
    virtual AttributeSubject *GetDefaultAtts();
    virtual void SetClientAtts(AttributeSubject *atts);
    virtual void GetClientAtts(AttributeSubject *atts);

    virtual avtPlot *AllocAvtPlot();

    virtual bool Permits2DViewScaling() const;
    virtual void InitializePlotAtts(AttributeSubject *atts, const avtPlotMetaData &);
    virtual const char *GetMenuName() const;
    static void InitializeGlobalObjects();
  private:
    static ParallelCoordinatesAttributes *defaultAtts;
    static ParallelCoordinatesAttributes *clientAtts;
};

class ParallelCoordinatesViewerPluginInfo : public virtual ViewerPlotPluginInfo, public virtual ParallelCoordinatesViewerEnginePluginInfo
{
  public:
    virtual const char **XPMIconData() const;

};

class ParallelCoordinatesEnginePluginInfo : public virtual EnginePlotPluginInfo, public virtual ParallelCoordinatesViewerEnginePluginInfo
{
  public:
};

class ParallelCoordinatesScriptingPluginInfo : public virtual ScriptingPlotPluginInfo, public virtual ParallelCoordinatesCommonPluginInfo
{
  public:
    virtual void InitializePlugin(AttributeSubject *subj, void *data);
    virtual void *GetMethodTable(int *nMethods);
    virtual bool TypesMatch(void *pyobject);
    virtual char *GetLogString();
    virtual void SetDefaults(const AttributeSubject *atts);
};

#endif
