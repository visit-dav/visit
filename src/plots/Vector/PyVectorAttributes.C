{
    VectorAttributesObject *newObject;
    newObject = PyObject_NEW(VectorAttributesObject, &VectorAttributesType);
    if(newObject == NULL)
        return NULL;
    newObject->data = (VectorAttributes *)attr;
    newObject->owns = false;
    return (PyObject *)newObject;
}

