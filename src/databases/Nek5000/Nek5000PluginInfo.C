// ****************************************************************************
//  Method:  Nek5000GeneralPluginInfo::GetDfltExtsFromGen
//
//  Purpose:
//    Returns the default extensions for a Nek5000 database.
//
//  Programmer:  generated by xml2info
//  Creation:    omitted
//
// ****************************************************************************
